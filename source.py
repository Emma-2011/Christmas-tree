import turtle
import random
turtle.bgcolor("lightblue")
t=turtle.Pen()

t.up()
t.goto(0,0)
t.pendown()
t.pensize(5)
t.pencolor("brown")
t.fillcolor("brown")
t.begin_fill()
t.forward(50)
t.right(90)
t.forward(75)
t.right(90)
t.forward(50)
t.right(90)
t.forward(75)
t.end_fill()
t.pencolor("green")
t.fillcolor("green")
t.begin_fill()
t.right(90)
t.forward(150)
t.left(120)
t.forward(120)
t.right(120)
t.forward(30)
t.left(120)
t.forward(120)
t.right(120)
t.forward(30)
t.left(120)
t.forward(140)
t.left(120)
t.forward(140)
t.left(120)
t.forward(30)
t.right(120)
t.forward(120)
t.left(120)
t.forward(30)
t.right(120)
t.forward(120)
t.left(120)
t.forward(120)
t.end_fill()
t.penup()
t.goto(20,350)
t.pendown()
t.pencolor("gold")
t.fillcolor("gold")
t.begin_fill()
t.right(72)
t.forward(50)
t.right(144)
t.forward(50)
t.right(144)
t.forward(50)
t.right(144)
t.forward(50)
t.right(144)
t.forward(50)
t.right(144)
t.hideturtle()
t.end_fill()
t.pensize(2)
t.pencolor("white")
def move():
    t.penup()
    x=random.randint(-350,350)
    y=random.randint(0,350)
    t.goto(x,y)
    t.pendown()
def draw_snow():
    n=random.randint(5,10)
    size=random.randint(4,12)
    for j in range(n):
        t.forward(size)
        t.forward(-size)
        t.right(360/n)
for i in range(50):
    move()
    draw_snow()

t.hideturtle()
turtle.done()
